    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(this.txtCodigo.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this,
                   "Debe Ingresar el Codigo del Producto",
                   "Error de Ingreso",
                  JOptionPane.ERROR_MESSAGE);
        this.txtCodigo.requestFocus();
        return;
        }
        int numeroserie = Integer.parseInt(this.txtCodigo.getText());
        Negocio neg = new Negocio();
        ProductoComprado buscar = neg.buscarProducto(numeroserie);
        if (buscar.getCodigoCompra()!= 0) {
            this.txtNombre.setText(buscar.getNombreProducto());
            this.txtMarca.setText(buscar.getMarcaProducto());
            this.txtModelo.setText(buscar.getModeloProducto());
            this.txtPrecio.setText("" + buscar.getPrecioProducto());
            this.txtFecha.setText(buscar.getFechaProducto());
            this.txtDetalle.setText(buscar.getDetalleProducto());
            
            this.txtCodigo.setEnabled(true);
            } 
           else {
            JOptionPane.showMessageDialog(this,
                    "Este Producto No Existe",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtCodigo.requestFocus();
            this.limpiarTextos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

